package main

import "fmt"

func main() {
	var num int
	fmt.Println("insira numero")
	fmt.Scan(&num)
	if num == 1 {
		fmt.Println("domigo")
	}
	if num == 2 {
		fmt.Println("segunda")
	}
	if num == 3 {
		fmt.Println("terca")
	}
	if num == 4 {
		fmt.Println("quarta")
	}
	if num == 5 {
		fmt.Println("quinta")
	}
	if num == 6 {
		fmt.Println("sexta")
	}
	if num == 7 {
		fmt.Println("sabado")
	}

}
